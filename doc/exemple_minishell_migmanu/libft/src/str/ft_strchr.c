#include "../../includes/libft.h"

char	*ft_strchr(const char *s, int i)
{
	while (*s)
	{
		if (*s == (char)i)
			return ((char *)s);
		s++;
	}
	if (*s == (char)i)
		return ((char *)s);
	return (NULL);
}
