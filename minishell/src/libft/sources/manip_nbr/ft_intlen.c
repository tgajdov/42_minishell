#include "../../includes/libft.h"

int	ft_intlen(int n)
{
	int	i;

	i = 1;
	while (n >= 10 || n <= -10)
	{
		n /= 10;
		i++;
	}
	if (n < 0)
		i++;
	return (i);
}
